        var frm = $('#form1');
        frm.submit(function () {
            $.ajax({
                type: frm.attr('method'),
                url: frm.attr('action'),
                data: frm.serialize(),
                complete: function(success){
                    $('#submission1').fadeOut(500);
                    $('#results1').delay(500).fadeIn(500);
                }
            });
        });
